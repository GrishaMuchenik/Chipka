# Массивы данных об автомобилях
M = ["M1", "M2", "M3", "M4", "M5", "M6", "M7"]
GR = [5.5, 6.2, 6.8, 7.1, 5.9, 6.5, 6.7]
N = ["N1", "N2", "N3", "N4", "N5", "N6", "N7"]

# Получение данных об автомобилях
print("Данные об автомобилях:")
for i in range(len(M)):
    print(f"Марка: {M[i]}, Грузоподъемность: {GR[i]} т, Номерной знак: {N[i]}")

# Фильтрация автомобилей по грузоподъемности
result_list = []
for i in range(len(GR)):
    if 6 <= GR[i] <= 7:
        result_list.append((M[i], N[i]))

# Определение автомобиля с минимальной грузоподъемностью
min_gr = min(GR)
min_index = GR.index(min_gr)

# Вывод результатов
print("\nСписок автомобилей с грузоподъемностью 6-7 тонн:")
for car in result_list:
    print(f"Марка: {car[0]}, Номерной знак: {car[1]}")

print(f"\nАвтомобиль с минимальной грузоподъемностью: {M[min_index]}, {N[min_index]}")
